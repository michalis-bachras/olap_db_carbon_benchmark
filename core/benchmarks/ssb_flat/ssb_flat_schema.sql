SELECT
  lo_orderdate,
  lo_orderkey,
  lo_linenumber,
  lo_custkey,
  lo_suppkey,
  lo_partkey,
  lo_quantity,
  lo_extendedprice,
  lo_ordertotalprice,
  lo_discount,
  lo_revenue,
  lo_supplycost,
  lo_tax,
  lo_commitdate,
  lo_shipmode,
  c_name,
  c_address,
  c_city,
  c_nation,
  c_region,
  c_phone,
  c_mktsegment,
  s_name,
  s_address,
  s_city,
  s_nation,
  s_region,
  s_phone,
  p_name,
  p_mfgr,
  p_category,
  p_brand1,
  p_color,
  p_type,
  p_size,
  d_datekey,
  d_date,
  d_dayofweek,
  d_month,
  d_year,
  d_yearmonthnum,
  d_yearmonth,
  d_daynuminweek,
  d_daynuminmonth,
  d_daynuminyear,
  d_monthnuminyear,
  d_lastdayinweekfl,
  d_lastdayinmonthfl,
  d_holidayfl,
  d_weekdayfl
FROM
  lineorder,
  customer,
  supplier,
  part,
  dwdate
WHERE
  lo_custkey = c_custkey
  AND lo_suppkey = s_suppkey
  AND lo_partkey = p_partkey
  AND lo_orderdate = d_datekey;